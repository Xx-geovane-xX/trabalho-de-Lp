    public GasTipo ViewBean(){
        GasTipo gasTipo = new GasTipo();
        int id = Integer.valueOf(jTxtCodigo.getText());
        gasTipo.setGasIdTipo(id);
        gasTipo.setGasDescricao(jTxtDescricao.getText());
        gasTipo.setGasNome(jTxtNome.getText());
        gasTipo.setGasPeso(jTxtPeso.getText());
        gasTipo.setGasUnidadeDeMedida(jTxtUnidadeDeMedida.getText());
        gasTipo.setGasPrecoMedio(jTxtPrecoMedio.getText());
        
        
        return gasTipo;
    }
    
    public void BeanView(GasTipo gasTipo){
        String id = String.valueOf(gasTipo.getGasIdTipo());
        jTxtCodigo.setText(id);
        jTxtDescricao.setText(gasTipo.getGasDescricao());
        jTxtNome.setText(gasTipo.getGasNome());
        jTxtPeso.setText(gasTipo.getGasPeso());
        jTxtUnidadeDeMedida.setText(gasTipo.getGasUnidadeDeMedida());
        jTxtPrecoMedio.setText(gasTipo.getGasPrecoMedio());
        
        
        
        
    }
    
