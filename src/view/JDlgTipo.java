    private void jBtnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnPesquisarActionPerformed
        // TODO add your handling code here:
        String resp = JOptionPane.showInputDialog(null, "Entre com a chave primaria (PK)", "Pesquisar" ,2);
        TipoDAO tipoDAO = new TipoDAO();
        int id = Integer.valueOf(resp);
        GasTipo gasTipo = (GasTipo) tipoDAO.list(id);
        BeanView(gasTipo);
    }//GEN-LAST:event_jBtnPesquisarActionPerformed

    private void jBtnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnExcluirActionPerformed
        // TODO add your handling code here:
        int resp =  JOptionPane.showConfirmDialog(null, "Confirma Exclusao", "Pergunta", JOptionPane.YES_NO_OPTION);
        if(resp == JOptionPane.YES_OPTION ){
            GasTipo gasTipo = ViewBean();
            TipoDAO tipoDAO = new TipoDAO();
            tipoDAO.delete(gasTipo);
        }else{
        JOptionPane.showMessageDialog(null, "Exclusao Cancelada");
        }
        
            
        
        
    }//GEN-LAST:event_jBtnExcluirActionPerformed

